% Правило для существования реакции при обратной последовательности
% реагирующих элементов (в реакциях не важен порядок)
reverse_reaction(Reaction, Elem1, Elem2) :-
    straight_reaction(Reaction, Elem2, Elem1).

% Универсальное правило реакции. Должна существовать реакция либо при
% прямом порядке элементов либо при обратном.
reaction(Reaction, Elem1, Elem2) :-
    straight_reaction(Reaction, Elem1, Elem2); reverse_reaction(Reaction, Elem1, Elem2).


%Персонаж дерется из дали если использует лук или катализатор
is_range(Name) :-
    weapon(Name, 'Bow'); weapon(Name, 'Catalyst').

% Персонаж дерется в ближнем бою если использует меч, двуруч. меч или
% копье
is_melee(Name) :-
    weapon(Name, 'Sword'); weapon(Name, 'Claymore'); weapon(Name, 'Polearm').

% Персонажи могут создать реакцию если владеют двумя реагирующими
% между собой элементами (порядок не важен)
can_have_reaction(Name1, Name2) :-
    element(Name1, Element1), element(Name2, Element2), reaction(_, Element1, Element2).

%Проверка из одного ли региона персонажи
from_same_region(Name1, Name2) :-
    region(Name1, Region), region(Name2, Region).
%Проверка имеют ли одинаковый элемент персонажи

have_same_element(Name1, Name2) :-
    element(Name1, Element), element(Name2, Element).

%Проверка является ли персонаж легендарным (5 звезд)
is_legendary(Name) :-
    quality(Name, 5).

%Проверка является ли персонаж эпическим (4 звезды)
is_epic(Name) :-
    quality(Name, 4).

